/*NOTA: Existen varios tipos de operadores aritméticos en JS
los cuales son los mismos que matemática por ejemplo:

OPERADOR       DESCRIPCIÓN

    +          Adicion
    -          substraccion
    *          Multiplicacion
    **         Exponenciacion
    /          Division
    %          Reciduo
    ++         Incremento
    --         Decremento

*/

let a = 5, b = 6;

let suma = a + b;
console.log(suma);

let resta = a - b;
console.log(resta);

let multiplicacion = a * b;
console.log(multiplicacion);


let expoonenciacion = a ** b;
console.log(expoonenciacion);

let divicion = a / b;
console.log(divicion);

