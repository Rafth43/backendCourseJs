/*
NOTA: Los operadores de asignación sirven para asignar, igualar,
diferenciar y verificar los valores de variables dadas existen
varios tipos de operadores de asignación en JS por ejemplo:
*/

// VALOR DE ASIGNACIÓN SIMPLE ( = )

/*
NOTA: la función del operador de asignación simple es
igualar una variable al valor asignad por ejemplo:
*/

let a = 1, b = 2 , c = 3 , d = 4, e = 2, f = 8, g = 5, h = 1;
a = 1;
console.log(a);

// VALOR DE ASIGNACIÓN COMPUESTO ( +=, -=, *=, %=, /=, **=)

/*
NOTA: la función de los operadores de asignación compuesto
es combinar una operación de asignación con otra operación 
por ejemplo:
*/

b += 3;
d -= 5;
e *= 2;
f %= 3;
g /= 5;
h **= 6;

console.log(b);
console.log(d);
console.log(c);
console.log(d);
console.log(e);
console.log(f);
console.log(g);
console.log(h);