/*
NOTA: El operador condicional (ternario) es el único operador en 
JavaScript que tiene tres operandos. Este operador se usa con 
frecuencia como atajo para la instrucción if.
*/

let resultado = (1>2) ? "Verdadero" : "Falso";
console.log(resultado);

let numero = 6;
let numeroPar = (numero % 2 == 0) ? "Es par" : "Es impar";
console.log(numeroPar);